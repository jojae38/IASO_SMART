void Control_Beep(IASO_SMART_STATE*STA)
{
	static char Temp;		// N times operating
	static char i=0;

	if(STA->Beep_On==ON)
	{	BEEP_ON;
		TIM2->ARR=640-1;				// Fixed Auto-Reloaded value
		TIM2->CCR1 = TIM2->ARR/2;		// 50% duty
		STA->Beep_Done=OFF;

		switch(STA->Beep_Type)
		{
			case OFF:
				Temp = 3;	// 3 sound
				TIM2->PSC = (uint32_t)100000/(uint32_t)Beep_OFF_Code[i];		// PWM is controled by Pre-scaler value
			break;
			case ON:
				Temp = 3;	// 3 sound
				TIM2->PSC = (uint32_t)100000/(uint32_t)Beep_ON_Code[i];		// PWM is controled by Pre-scaler value
			break;
			case MODE_CHANGE:
				Temp = 1;	// 1 sound
				TIM2->PSC = (uint32_t)100000/(uint32_t)Beep_MODE_CHANGE_Code[i];		// PWM is controled by Pre-scaler value
			break;
		}

		HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
		if(STA->Sound_Tick>STA->Sound_Tick_Cnt)
		{
			i++;
			STA->Sound_Tick=0;
		}
		if(i>=Temp)
		{
			HAL_TIM_PWM_Stop(&htim2, TIM_CHANNEL_1);
			STA->Beep_On = OFF;
			STA->Beep_Done = ON;
			i=0;
			BEEP_OFF;
		}
	}
}

